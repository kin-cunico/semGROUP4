package com.napier.semGROUP4;

/**
 * Represents a city
 */
public class City
{
    /**
     * City name
     */
    public String name;

    /**
     * Country the city belongs to
     */
    public String country;

    /**
     * District of the city
     */
    public String district;

    /**
     * Population of the city
     */
    public int population;
}