package com.napier.semGROUP4;

/**
 * Represents a country
 */
public class Country
{
    /**
     * Country code
     */
    public String code;

    /**
     * Country name
     */
    public String name;

    /**
     * Country's continent
     */
    public String continent;

    /**
     * Country's region
     */
    public String region;

    /**
     * Country's population
     */
    public int population;

    /**
     * Country's capital city
     */
    public String capital;
}